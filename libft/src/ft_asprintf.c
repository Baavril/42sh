#include <stdarg.h>
#include <unistd.h>

#include "libft.h"

int		ft_asprintf(char **ret, const char *format, ...)
{
	va_list ap;
	int		value;

	va_start(ap, format);
	value = ft_vasprintf(ret, format, ap);
	va_end(ap);
	return (value);
}
