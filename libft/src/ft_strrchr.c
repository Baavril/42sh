char		*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*r;

	i = 0;
	r = 0;
	while (s[i] && (c >= 0 && c <= 127))
	{
		if (s[i] == c)
			r = (char*)&s[i];
		++i;
	}
	if (s[i] == c)
		return ((char*)&s[i]);
	return (r);
}
