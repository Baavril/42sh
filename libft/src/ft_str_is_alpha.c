int	ft_str_is_alpha(char *s)
{
	if (!*s)
		return (1);
	while ((*s >= 97 && *s <= 122) || (*s >= 65 && *s <= 90))
	{
		++s;
		if (!*s)
			return (1);
	}
	return (0);
}
