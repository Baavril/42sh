int		ft_chrsearch(const char *s, int c)
{
	int	i;

	i = 0;
	if (s)
	{
		while (s[i] && (c >= 0 && c <= 127))
		{
			if (s[i] == c)
				return (1);
			++i;
		}
	}
	return (0);
}
