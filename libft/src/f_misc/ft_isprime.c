#include <stdlib.h>

int	ft_isprime(size_t n)
{
	size_t	i;

	i = 2;
	if (n < 2)
		return (0);
	while (i <= n / 2)
	{
		if (n % i == 0)
			return (0);
		i++;
	}
	return (1);
}
